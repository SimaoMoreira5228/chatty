fn rgb(hex: u32) -> Color {
	let r = ((hex >> 16) & 0xff) as f32 / 255.0;
	let g = ((hex >> 8) & 0xff) as f32 / 255.0;
	let b = (hex & 0xff) as f32 / 255.0;
	Color::from_rgb(r, g, b)
}

